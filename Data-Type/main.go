package main

import "fmt"

func main() {

	// Decimal
	var decimal = 7.77
	fmt.Printf("decimal : %f\n", decimal)
	fmt.Printf("decimal : %f\n", decimal)

	// Integer
	var integer int = 7
	fmt.Printf("integer : %d\n", integer)
	fmt.Printf("integer : %d\n", integer)

	// Boolean
	var status bool = true
	fmt.Printf("status : %t\n", status)

	// String
	var waifu string = "Rem"
	fmt.Printf("%s sayang re-kun >//<", waifu)

}
